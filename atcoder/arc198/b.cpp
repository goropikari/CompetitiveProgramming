void yesno(bool x) {
    cout << (x ? "Yes" : "No") << '\n';
}

void solve();

int main() {
    solve();
    return 0;
}

void solve() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    rep(_, t) {
        ll x, y, z;
        cin >> x >> y >> z;

        if (x < z) {
            yesno(false);
            continue;
        };

        if (y <= 2 * (x - z)) {
            y %= 2;
        } else {
            y -= 2 * (x - z);
        }
        yesno(y <= z * 2);
    }
}
