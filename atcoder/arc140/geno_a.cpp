void solve() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, K, cnt[26];
    string s;
    cin >> n >> K >> s;
    int ans = INT_MAX;
    for (int i = 1; i <= n; i++)
        if (!(n % i)) {
            int mx = 0;
            for (int j = 0; j < i; j++) {
                memset(cnt, 0, sizeof(cnt));
                for (int k = j; k < n; k += i)
                    cnt[s[k] - 'a']++;
                mx += *max_element(cnt, cnt + 26);
            }
            if (n - mx <= K)
                ans = min(ans, i);
        }
    cout << ans << "\n";
}
